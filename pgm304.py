#304. Program prompt and accept two numbers from user and find the difference

#Initialisation
print("Enter the first number : ")
first_number = input()
a = int(first_number)

print("Enter the second number : ")
second_number = input()
b = int(second_number)

#Computation
difference = a - b

#Display
print("The difference of", a, "and", b, "is =", difference)
